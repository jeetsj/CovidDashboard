        <script type="text/javascript">
            $("#add-trip").submit(function (event) {
                event.preventDefault();
                var $form = $(this);
                var sname = $form.find("input[name='sname']").val();
                var date = $form.find("input[name='date']").val();
                var time = $form.find("input[name='time']").val();
                var cdetail = $form.find("textarea[name='cdetail']").val();
                var summary = $form.find("textarea[name='summary']").val();
                var url = "http://localhost:3000/trip/add";
                var decodedCookie = decodeURIComponent(document.cookie);
                var ca = decodedCookie.split(';');
                var ca2 = ca[0].slice(6,);
                var data = {
                    sname: sname,
                    date: date,
                    time: time,
                    cdetail: cdetail,
                    summary: summary
                }
                $.ajax({
                    type: "POST",
                    url: url,
                    data: JSON.stringify(data),
                    contentType: "application/json",
                    headers: {
                        "Authorization": `Bearer ${ca2}`
                    },
                    success: function (response) {
                        // console.log(response)
                        // alert(response);
                        if (response.token) {
                            alert(response);
                        }
                        else {
                            location.href = "all-trips.php";
                        }
                    }
                });
            });
        </script>
